    echo "‚ùå Import failed - there may be other issues"
fi

echo ""
echo "================================================"
echo "‚úÖ Setup Complete!"
echo "================================================"
echo ""
echo "Next steps:"
echo "1. Make sure your .env file has correct database credentials"
