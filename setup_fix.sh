#!/bin/bash
# Setup script to fix database.py import issues
# Run this script in your project root directory

echo "================================================"
echo "üîß Fixing Database Import Structure"
echo "================================================"

# Check if database.py exists
if [ ! -f "database.py" ] && [ ! -f "utils/database.py" ]; then
    echo "‚ùå Error: database.py not found!"
    echo "Please make sure database.py exists in your project"
    exit 1
fi

# Create utils directory if it doesn't exist
if [ ! -d "utils" ]; then
    echo "üìÅ Creating utils/ directory..."
    mkdir -p utils
    echo "‚úÖ utils/ created"
else
    echo "‚úÖ utils/ already exists"
fi

# Create __init__.py if it doesn't exist
if [ ! -f "utils/__init__.py" ]; then
    echo "üìù Creating utils/__init__.py..."
    touch utils/__init__.py
    echo "‚úÖ utils/__init__.py created"
else
    echo "‚úÖ utils/__init__.py already exists"
fi

# Move database.py if it's in root
if [ -f "database.py" ] && [ ! -f "utils/database.py" ]; then
    echo "üì¶ Moving database.py to utils/..."
    mv database.py utils/
    echo "‚úÖ database.py moved to utils/"
elif [ -f "utils/database.py" ]; then
    echo "‚úÖ utils/database.py already in correct location"
fi

# Verify structure
echo ""
echo "üìã Verifying project structure:"
echo "---"

if [ -d "utils" ]; then
    echo "‚úÖ utils/ folder exists"
else
    echo "‚ùå utils/ folder missing"
fi

if [ -f "utils/__init__.py" ]; then
    echo "‚úÖ utils/__init__.py exists"
else
    echo "‚ùå utils/__init__.py missing"
fi

if [ -f "utils/database.py" ]; then
    echo "‚úÖ utils/database.py exists"
else
    echo "‚ùå utils/database.py missing"
fi

# Check Python packages
echo ""
echo "üì¶ Checking Python packages:"
echo "---"

python3 -c "import pymysql" 2>/dev/null && echo "‚úÖ pymysql installed" || echo "‚ùå pymysql not installed - run: pip install pymysql"
python3 -c "import pandas" 2>/dev/null && echo "‚úÖ pandas installed" || echo "‚ùå pandas not installed - run: pip install pandas"
python3 -c "import dotenv" 2>/dev/null && echo "‚úÖ python-dotenv installed" || echo "‚ùå python-dotenv not installed - run: pip install python-dotenv"
python3 -c "import streamlit" 2>/dev/null && echo "‚úÖ streamlit installed" || echo "‚ùå streamlit not installed - run: pip install streamlit"

# Test import
echo ""
echo "üß™ Testing import:"
echo "---"

python3 -c "from utils.database import test_connection; print('‚úÖ Import successful!')" 2>/dev/null && import_ok=1 || import_ok=0

if [ $import_ok -eq 1 ]; then
    echo "‚úÖ Database module imports correctly!"
    
    # Test connection
    echo ""
    echo "üîå Testing database connection:"
    echo "---"
    python3 -c "from utils.database import test_connection; result = test_connection(); print('‚úÖ Connection successful!' if result else '‚ùå Connection failed - check .env file')" 2>/dev/null || echo "‚ùå Connection test failed"
